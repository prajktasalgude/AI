public class PragatiScholarship {
	
	private int marksOfLastYear;
	private boolean married;
	private int noOfSiblingsAlreadyGotThisScholarship;
	private int annualIncome;
	private String aadharNumber;
	private String caste;
	private String address;
	
	public int getMarksOfLastYear() {
		return marksOfLastYear;
	}
	public void setMarksOfLastYear(int marksOfLastYear) {
		this.marksOfLastYear = marksOfLastYear;
	}
	public boolean isMarried() {
		return married;
	}
	public void setMarried(boolean married) {
		this.married = married;
	}
	public int getNoOfSiblingsAlreadyGotThisScholarship() {
		return noOfSiblingsAlreadyGotThisScholarship;
	}
	public void setNoOfSiblingsAlreadyGotThisScholarship(int noOfSiblingsAlreadyGotThisScholarship) {
		this.noOfSiblingsAlreadyGotThisScholarship = noOfSiblingsAlreadyGotThisScholarship;
	}
	public int getAnnualIncome() {
		return annualIncome;
	}
	public void setAnnualIncome(int annualIncome) {
		this.annualIncome = annualIncome;
	}
	public String getAadharNumber() {
		return aadharNumber;
	}
	public void setAadharNumber(String aadharNumber) {
		this.aadharNumber = aadharNumber;
	}
	public String getCaste() {
		return caste;
	}
	public void setCaste(String caste) {
		this.caste = caste;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
	
}